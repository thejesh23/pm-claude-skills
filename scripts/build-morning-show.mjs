#!/usr/bin/env node
// The Morning Show / Weekly Newsletter (moonshot: passive audience, no new work).
// Deterministically composes a weekly issue from the catalog — a skill of the
// week, fresh picks, the numbers, and a short spoken "show script" ready for TTS.
// No API, no cost: it's reproducible from the repo data and rotates by ISO week,
// so a scheduled Action can publish an issue every week untouched.
//
//   node scripts/build-morning-show.mjs
import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const SITE = 'https://mohitagw15856.github.io/pm-claude-skills';
const OUT = join(root, 'newsletter');

const raw = JSON.parse(readFileSync(join(root, 'web', 'skills.json'), 'utf8'));
const skills = Array.isArray(raw) ? raw : raw.skills || [];
const bundles = new Set(skills.map((s) => s.plugin).filter(Boolean));
const scored = skills.filter((s) => s.eval && s.eval.score);

// ISO-week index → a stable, rotating pick each week (deterministic, no randomness).
const now = new Date();
const jan1 = new Date(Date.UTC(now.getUTCFullYear(), 0, 1));
const week = Math.floor((now - jan1) / (7 * 864e5));
const issue = week + 1;
const date = now.toISOString().slice(0, 10);
const pick = (arr, offset) => arr[((week * 7 + offset) % arr.length + arr.length) % arr.length];

const oneLiner = (s) => (s.summary || s.description || '').split(/(?<=[.!?])\s/)[0];
const link = (s) => `${SITE}/?skill=${encodeURIComponent(s.name)}`;

// Prefer proven skills for the headliner; fresh picks span the catalog.
const proven = scored.length ? scored.slice().sort((a, b) => (b.eval.score || 0) - (a.eval.score || 0)) : skills;
const star = pick(proven, 0);
const fresh = [pick(skills, 3), pick(skills, 11), pick(skills, 19)].filter((s, i, a) => s && a.indexOf(s) === i);

const md = `# PM Skills Weekly — Issue ${issue}
**${date}**

Your weekly minute on doing professional work with AI — one skill to master, a few to try, and the numbers.

## 🌟 Skill of the week: ${star.title || star.name}
${oneLiner(star)}
**→ [Run it free](${link(star)})** · \`${star.name}\`${star.eval ? ` · ✅ ${star.eval.score}/5` : ''}

## 🔥 Fresh picks
${fresh.map((s) => `- **[${s.title || s.name}](${link(s)})** — ${oneLiner(s)}`).join('\n')}

## 📊 By the numbers
**${skills.length}** professional skills · **${bundles.size}** bundles · **${scored.length}** eval-scored${scored.length ? ` (avg ${(scored.reduce((a, s) => a + s.eval.score, 0) / scored.length).toFixed(1)}/5)` : ''}.

---

### 🎙️ Show script (for TTS / the podcast)
> Good morning — this is PM Skills Weekly, issue ${issue}. This week's skill to master is **${star.title || star.name}**: ${oneLiner(star).toLowerCase()} If you only try one thing this week, make it that one. A few fresh picks worth a look: ${fresh.map((s) => s.title || s.name).join(', ')}. The library now stands at ${skills.length} skills across ${bundles.size} bundles${scored.length ? `, ${scored.length} of them eval-scored` : ''}. Run any of them free in your browser — no signup. That's your minute. See you next week.

---

*Browse everything at [${SITE.replace('https://', '')}](${SITE}) · [subscribe / archive](README.md) · open-source (MIT).*
`;

if (!existsSync(OUT)) mkdirSync(OUT, { recursive: true });
const file = `${now.getUTCFullYear()}-w${String(issue).padStart(2, '0')}.md`;
writeFileSync(join(OUT, file), md);

// Rebuild a simple archive index.
const issues = readdirSync(OUT).filter((f) => /^\d{4}-w\d+\.md$/.test(f)).sort().reverse();
const index = `# PM Skills Weekly

A once-a-week minute on doing professional work with AI — auto-generated from the catalog, so it ships with no manual effort. Each issue also carries a spoken **show script** ready for text-to-speech.

## Issues
${issues.map((f) => `- [${f.replace('.md', '')}](${f})`).join('\n')}

_Generated by \`scripts/build-morning-show.mjs\` (weekly Action). To turn it into a real email/podcast, pipe the latest issue into your ESP or a TTS step._
`;
writeFileSync(join(OUT, 'README.md'), index);
console.log(`Wrote newsletter/${file} (issue ${issue}) + archive index.`);
