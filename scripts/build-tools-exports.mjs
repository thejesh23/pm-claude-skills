#!/usr/bin/env node
// Compile the skill library into agent-framework TOOLS — so any agent built with
// the OpenAI SDK, Vercel AI SDK, LangChain, or anything that speaks JSON-Schema
// function calling can wield the skills natively. Generates the tools-pkg/
// subpackage (npm: pm-skills-tools). Regenerate when skills change.
//
// The execution model (stated plainly): calling a skill-tool returns the skill's
// full instructions plus the input, for the CALLING model to execute — the same
// pattern as the MCP server's get_skill. No second API key, works everywhere,
// and the agent's own model (which the builder already trusts) does the work.
import { readFileSync, readdirSync, existsSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const catalog = JSON.parse(readFileSync(join(root, 'web', 'skills.json'), 'utf8'));
const outDir = join(root, 'tools-pkg');
mkdirSync(outDir, { recursive: true });

const tools = [];
for (const s of catalog.skills) {
  const p = join(root, 'skills', s.name, 'SKILL.md');
  if (!existsSync(p)) continue;
  const text = readFileSync(p, 'utf8');
  const body = (text.match(/^---\r?\n[\s\S]*?\r?\n---\r?\n?([\s\S]*)$/) || [, text])[1].trim();
  tools.push({
    name: s.name.replace(/-/g, '_'),
    skill: s.name,
    bundle: s.plugin || 'other',
    description: String(s.description || '').slice(0, 1024),
    instructions: body,
  });
}

// 1. OpenAI-style function schemas (also valid for any JSON-Schema tool caller).
const openai = tools.map((t) => ({
  type: 'function',
  function: {
    name: t.name,
    description: t.description,
    parameters: {
      type: 'object',
      properties: { input: { type: 'string', description: "The user's raw input for this skill — notes, brief, data, or task." } },
      required: ['input'],
    },
  },
}));
writeFileSync(join(outDir, 'openai-tools.json'), JSON.stringify(openai, null, 1) + '\n');

// 2. The runtime package: definitions + executors + selection helpers.
const runtime = `// pm-skills-tools — the pm-claude-skills library (${tools.length} skills) as agent tools.
// Generated by scripts/build-tools-exports.mjs — do not edit by hand.
//
//   import { pick, search, toOpenAI, toVercelAI } from 'pm-skills-tools';
//   const tools = pick(['prd_template', 'meeting_notes']);          // never ship all ${tools.length} to one context
//   const openaiTools = toOpenAI(tools);                            // OpenAI / any JSON-schema caller
//   const aiSdkTools  = toVercelAI(tools);                          // Vercel AI SDK ({ [name]: {description, parameters, execute} })
//
// Executing a tool returns the skill's full instructions + the input for YOUR
// model to carry out (the get_skill pattern): no extra key, no second vendor.
import { readFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const INDEX = JSON.parse(readFileSync(join(here, 'index.json'), 'utf8'));
const body = (name) => readFileSync(join(here, 'instructions', name + '.md'), 'utf8');

export const names = INDEX.map((t) => t.name);
export function get(name) {
  const t = INDEX.find((x) => x.name === name || x.skill === name);
  if (!t) throw new Error('Unknown skill tool: ' + name);
  return { ...t, instructions: body(t.skill) };
}
export function pick(list) { return list.map(get); }
export function search(query, limit = 5) {
  const terms = String(query).toLowerCase().split(/\\s+/).filter(Boolean);
  return INDEX.map((t) => {
    const hay = (t.name + ' ' + t.description).toLowerCase();
    let score = 0;
    for (const w of terms) if (hay.includes(w)) score += t.name.includes(w) ? 2 : 1;
    return { t, score };
  }).filter((x) => x.score > 0).sort((a, b) => b.score - a.score).slice(0, limit).map((x) => get(x.t.name));
}
const directive = (t, input) =>
  'You now have the following professional skill loaded. Execute it on the input and produce the complete artifact.\\n\\n' +
  t.instructions + '\\n\\n---\\nINPUT:\\n' + input;

export function toOpenAI(tools) {
  return tools.map((t) => ({ type: 'function', function: { name: t.name, description: t.description,
    parameters: { type: 'object', properties: { input: { type: 'string', description: "The user's raw input for this skill." } }, required: ['input'] } } }));
}
export function executeOpenAI(tools, name, args) {
  const t = tools.find((x) => x.name === name); if (!t) throw new Error('Unknown tool: ' + name);
  return directive(t, (typeof args === 'string' ? JSON.parse(args) : args).input || '');
}
export function toVercelAI(tools) {
  const out = {};
  for (const t of tools) out[t.name] = {
    description: t.description,
    parameters: { type: 'object', properties: { input: { type: 'string' } }, required: ['input'] },
    execute: async ({ input }) => directive(t, input || ''),
  };
  return out;
}
`;
writeFileSync(join(outDir, 'tools.mjs'), runtime);
writeFileSync(join(outDir, 'index.json'), JSON.stringify(tools.map(({ instructions, ...rest }) => rest), null, 1) + '\n');
mkdirSync(join(outDir, 'instructions'), { recursive: true });
for (const t of tools) writeFileSync(join(outDir, 'instructions', t.skill + '.md'), t.instructions + '\n');
console.log(`Wrote tools-pkg/: ${tools.length} tools (openai-tools.json, tools.mjs, index.json, instructions/)`);
