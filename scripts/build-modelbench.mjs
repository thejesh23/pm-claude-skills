#!/usr/bin/env node
// Builds web/modelbench.json — the render-ready model leaderboard for the
// public SkillBench page. Reads the reproducible results in
// skillbench/results.json (+ tasks.json for domain labels) and flattens them
// into per-model rows with a per-domain skilled breakdown. Mirrors the
// build-leaderboard pattern; run at deploy time and committed so the page
// works immediately.
//
//   node scripts/build-modelbench.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const results = JSON.parse(readFileSync(join(root, 'skillbench', 'results.json'), 'utf8'));
const taskSet = JSON.parse(readFileSync(join(root, 'skillbench', 'tasks.json'), 'utf8'));
const tasks = taskSet.tasks || [];
const domains = [...new Set(tasks.map((t) => t.domain))];

// Latest run per model wins (by date), so re-runs supersede old numbers.
const byModel = new Map();
for (const r of results.runs || []) {
  const prev = byModel.get(r.model);
  if (!prev || (r.date || '') >= (prev.date || '')) byModel.set(r.model, r);
}

const models = [...byModel.values()].map((r) => {
  const byDomain = {};
  for (const d of domains) {
    const ts = (r.tasks || []).filter((t) => t.domain === d);
    if (ts.length) byDomain[d] = +(ts.reduce((a, t) => a + (t.skilled || 0), 0) / ts.length).toFixed(2);
  }
  return {
    model: r.model,
    score: r.score, bare: r.bare, lift: r.lift,
    date: r.date, judge: r.judge,
    taskSetVersion: r.taskSetVersion, harness: r.harness,
    byDomain,
  };
}).sort((a, b) => (b.score || 0) - (a.score || 0));

const out = {
  _comment: 'Generated by scripts/build-modelbench.mjs from skillbench/results.json — do not edit by hand.',
  generated: new Date().toISOString(),
  taskCount: tasks.length,
  taskSetVersion: taskSet.version,
  domains,
  models,
};
writeFileSync(join(root, 'web', 'modelbench.json'), JSON.stringify(out, null, 2) + '\n');
console.log(`Wrote web/modelbench.json — ${models.length} model(s), ${tasks.length} tasks across ${domains.length} domains.`);
